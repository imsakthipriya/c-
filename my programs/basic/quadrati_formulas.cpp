#include <iostream>
using namespace std;
int main()
{

    int a, b;
    cin >> a;
    cin >> b;
    cout << "enter a : " << a << endl;
    cout << "enter b : " << b << endl;
    cout << "(a+b)^2 : " << (a + b) * (a + b) << endl;
    cout << "(a-b)^2 : " << (a - b) * (a - b) << endl;
    cout << "a^2 - b^2 : " << (a * a) - (b * b) << endl;
}